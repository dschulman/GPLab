# ╔═╡ a3c18a4b-3991-468e-a2ce-a70c7f8fb7c6
function lml(x, ystats, mean, noise_var, kernel)
	n = length(x)
	C = kernelmatrix(kernel, x) + Diagonal(noise_var ./ ystats.m)
	Cchol = cholesky(C)
	z = ystats.u .- mean
	return logV(ystats, noise_var) - (1/2) * (
		(n * log2π) +
		logdet(Cchol) +
		(z' * (Cchol \ z))
	)
end

