# ╔═╡ bda051b6-cbb2-4266-aebc-bca0069385df
function fg!(F, G, θ)
	if G !== nothing
		val, grad = Zygote.withgradient(objective, θ)
		copyto!(G, only(grad))
		if F !== nothing
			return val
		end
	end
	if F !== nothing
		return objective(θ)
	end
	return nothing
end

