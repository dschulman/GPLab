# ╔═╡ ff2b7ead-e29c-4bdd-bbbe-bb680003e4c0
function logV(ystats, noise_var)
	n = length(x)
	sum_m = sum(ystats.m)
	M = Diagonal(ystats.m)
	return -(1/2) * (
		((sum_m - n) * (log2π + log(noise_var))) +
		logdet(M) +
		((ystats.m ⋅ ystats.v) / noise_var)
	)
end

